package com.qlangtech.tis.workflow.pojo;

import com.qlangtech.tis.plugin.IdentityName;

import java.io.Serializable;
import java.util.Date;

/**
 * @author 百岁（baisui@qlangtech.com）
 * @date 2020/04/13
 */
public class DatasourceDb implements Serializable, IdentityName {

  private Integer id;

  private String name;

  private String extendClass;

  private Byte syncOnline;

  private Date createTime;

  private Date opTime;

  private static final long serialVersionUID = 1L;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name == null ? null : name.trim();
  }

  public Byte getSyncOnline() {
    return syncOnline;
  }

  public void setSyncOnline(Byte syncOnline) {
    this.syncOnline = syncOnline;
  }

  public Date getCreateTime() {
    return createTime;
  }

  public void setCreateTime(Date createTime) {
    this.createTime = createTime;
  }

  public Date getOpTime() {
    return opTime;
  }

  public void setOpTime(Date opTime) {
    this.opTime = opTime;
  }

  public String getExtendClass() {
    return this.extendClass;
  }

  public void setExtendClass(String extendClass) {
    this.extendClass = extendClass;
  }

  @Override
  public String identityValue() {
    return this.name;
  }
}
